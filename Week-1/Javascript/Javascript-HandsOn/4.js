//Javascript Operators
let a = 10; // Number variable
let b = 20; // Number variable
let c = a + b; // Addition operator
let d = a - b; // Subtraction operator
let e = a * b; // Multiplication operator
let f = a / b; // Division operator
let g = a % b; // Modulus operator
let h = a ** 2; // Exponentiation operator
let i = a++; // Postfix increment operator
let j = ++b; // Prefix increment operator
let k = a--; // Postfix decrement operator
let l = --b; // Prefix decrement operator
let m = a == b; // Equality operator
let n = a != b; // Inequality operator
let o = a > b; // Greater than operator
let p = a < b; // Less than operator
let q = a >= b; // Greater than or equal to operator
let r = a <= b; // Less than or equal to operator
let s = (a == b) ? "Equal" : "Not Equal"; // Ternary operator
let t = (a > b) && (b > 0); // Logical AND operator
let u = (a < b) || (b < 0); // Logical OR operator
let v = !(a < b); // Logical NOT operator
console.log(`c=${c}, d=${d}, e=${e}, f=${f}, g=${g}, h=${h}`); // Output: c=30, d=-10, e=200, f=0.5, g=10, h=100
console.log(`i=${i}, j=${j}, k=${k}, l=${l}`); // Output: i=10, j=21, k=11, l=20
console.log(`m=${m}, n=${n}, o=${o}, p=${p}, q=${q}, r=${r}`); // Output: m=false, n=true, o=false, p=true, q=false, r=true
console.log(`s=${s}, t=${t}, u=${u}, v=${v}`); // Output: s=Not Equal, t=true, u=true, v=false
